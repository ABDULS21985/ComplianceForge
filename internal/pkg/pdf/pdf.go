package pdf

import (
	"bytes"
	"fmt"
	"strings"
	"time"

	"github.com/go-pdf/fpdf"
)

// PDFGenerator defines the interface for generating PDF reports.
type PDFGenerator interface {
	// GenerateComplianceReport produces a PDF compliance report from the given data.
	GenerateComplianceReport(data interface{}) ([]byte, error)

	// GenerateRiskReport produces a PDF risk assessment report from the given data.
	GenerateRiskReport(data interface{}) ([]byte, error)

	// GenerateAuditReport produces a PDF audit report from the given data.
	GenerateAuditReport(data interface{}) ([]byte, error)
}

// ReportPDFGenerator produces auditor-grade PDF reports with cover pages,
// executive summaries, data tables, traffic-light indicators, and watermarks.
type ReportPDFGenerator struct {
	companyName    string
	logoPath       string
	classification string
}

// NewReportPDFGenerator creates a new ReportPDFGenerator.
// companyName is printed on the cover page, logoPath is an optional path to a
// company logo image, and classification (e.g. "CONFIDENTIAL") is stamped on
// every page header/footer.
func NewReportPDFGenerator(companyName, logoPath, classification string) *ReportPDFGenerator {
	return &ReportPDFGenerator{
		companyName:    companyName,
		logoPath:       logoPath,
		classification: classification,
	}
}

