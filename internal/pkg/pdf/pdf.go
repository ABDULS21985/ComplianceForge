// newDoc initialises an A4 PDF with headers and footers.
func (g *ReportPDFGenerator) newDoc(title string) *fpdf.Fpdf {
	pdf := fpdf.New("P", "mm", "A4", "")
	pdf.SetTitle(title, true)
	pdf.SetAuthor("ComplianceForge", true)
	pdf.SetCreator("ComplianceForge GRC Platform", true)
	pdf.SetAutoPageBreak(true, 25)

	// Header on every page: report title (left) + page number (right).
	pdf.SetHeaderFuncMode(func() {
		pdf.SetFont("Helvetica", "I", 8)
		pdf.SetTextColor(128, 128, 128)
		pdf.CellFormat(0, 5, title, "", 0, "L", false, 0, "")
		pdf.CellFormat(0, 5, fmt.Sprintf("Page %d", pdf.PageNo()), "", 0, "R", false, 0, "")
		pdf.Ln(8)
	}, true)

	// Footer on every page: classification | generation date | branding.
	pdf.SetFooterFunc(func() {
		pdf.SetY(-20)
		pdf.SetFont("Helvetica", "I", 7)
		pdf.SetTextColor(128, 128, 128)
		classText := ""
		if g.classification != "" {
			classText = strings.ToUpper(g.classification) + " | "
		}
		pdf.CellFormat(0, 5,
			fmt.Sprintf("%sGenerated: %s | Generated by ComplianceForge",
				classText, time.Now().UTC().Format("02 Jan 2006 15:04 UTC")),
			"", 0, "C", false, 0, "")
	})

	return pdf
}

// addCoverPage renders a title page with classification stamp, report title,
// subtitle, organisation name, and generation date.
func (g *ReportPDFGenerator) addCoverPage(pdf *fpdf.Fpdf, title, subtitle string) {
	pdf.AddPage()
	pdf.SetFont("Helvetica", "", 12)

	// Top classification stamp
	if g.classification != "" {
		pdf.SetFont("Helvetica", "B", 14)
		pdf.SetTextColor(220, 38, 38) // red
		pdf.CellFormat(0, 15, strings.ToUpper(g.classification), "", 1, "C", false, 0, "")
	}

	pdf.Ln(50)

	// Title
	pdf.SetFont("Helvetica", "B", 28)
	pdf.SetTextColor(30, 58, 138) // indigo-900
	pdf.MultiCell(0, 12, title, "", "C", false)
	pdf.Ln(8)

	// Subtitle
	if subtitle != "" {
		pdf.SetFont("Helvetica", "", 14)
		pdf.SetTextColor(100, 100, 100)
		pdf.MultiCell(0, 8, subtitle, "", "C", false)
	}

	pdf.Ln(20)

	// Organisation name
	pdf.SetFont("Helvetica", "", 16)
	pdf.SetTextColor(50, 50, 50)
	pdf.CellFormat(0, 10, g.companyName, "", 1, "C", false, 0, "")

	// Date
	pdf.Ln(10)
	pdf.SetFont("Helvetica", "", 12)
	pdf.SetTextColor(100, 100, 100)
	pdf.CellFormat(0, 8, time.Now().UTC().Format("02 January 2006"), "", 1, "C", false, 0, "")
}

// addTableOfContents renders a simple table of contents page.
func (g *ReportPDFGenerator) addTableOfContents(pdf *fpdf.Fpdf, sections []string) {
	pdf.AddPage()
	g.addSectionTitle(pdf, "Table of Contents")

	pdf.SetFont("Helvetica", "", 11)
	pdf.SetTextColor(50, 50, 50)
	for i, s := range sections {
		pdf.CellFormat(0, 8, fmt.Sprintf("%d.  %s", i+1, s), "", 1, "L", false, 0, "")
	}
	pdf.Ln(4)
}

// addSectionTitle renders a numbered section heading with an underline.
func (g *ReportPDFGenerator) addSectionTitle(pdf *fpdf.Fpdf, title string) {
	pdf.Ln(8)
	pdf.SetFont("Helvetica", "B", 16)
	pdf.SetTextColor(30, 58, 138)
	pdf.CellFormat(0, 10, title, "", 1, "L", false, 0, "")
	// Underline
	pdf.SetDrawColor(30, 58, 138)
	pdf.SetLineWidth(0.5)
	y := pdf.GetY()
	pdf.Line(10, y, 200, y)
	pdf.Ln(6)
}

// addSubsectionTitle renders a subsection heading.
func (g *ReportPDFGenerator) addSubsectionTitle(pdf *fpdf.Fpdf, title string) {
	pdf.Ln(4)
	pdf.SetFont("Helvetica", "B", 12)
	pdf.SetTextColor(50, 50, 50)
	pdf.CellFormat(0, 8, title, "", 1, "L", false, 0, "")
	pdf.Ln(2)
}

// addParagraph renders body text.
func (g *ReportPDFGenerator) addParagraph(pdf *fpdf.Fpdf, text string) {
	pdf.SetFont("Helvetica", "", 10)
	pdf.SetTextColor(50, 50, 50)
	pdf.MultiCell(0, 5, text, "", "L", false)
	pdf.Ln(3)
}

// addKPIRow renders a row of key-performance-indicator cards.
func (g *ReportPDFGenerator) addKPIRow(pdf *fpdf.Fpdf, kpis []struct{ Label, Value, Color string }) {
	if len(kpis) == 0 {
		return
	}
	colWidth := 190.0 / float64(len(kpis))

	for _, kpi := range kpis {
		x := pdf.GetX()
		y := pdf.GetY()

		// Background box
		pdf.SetFillColor(245, 247, 250)
		pdf.RoundedRect(x, y, colWidth-3, 25, 2, "1234", "F")

		// Value
		pdf.SetFont("Helvetica", "B", 18)
		switch kpi.Color {
		case "green":
			pdf.SetTextColor(34, 197, 94)
		case "amber":
			pdf.SetTextColor(234, 179, 8)
		case "red":
			pdf.SetTextColor(239, 68, 68)
		default:
			pdf.SetTextColor(30, 58, 138)
		}
		pdf.SetXY(x+3, y+3)
		pdf.CellFormat(colWidth-6, 8, kpi.Value, "", 0, "C", false, 0, "")

		// Label
		pdf.SetFont("Helvetica", "", 8)
		pdf.SetTextColor(100, 100, 100)
		pdf.SetXY(x+3, y+14)
		pdf.CellFormat(colWidth-6, 5, kpi.Label, "", 0, "C", false, 0, "")

		pdf.SetXY(x+colWidth, y)
	}
	pdf.Ln(30)
}

// addTable renders a data table with a coloured header row and alternating
// row colours. Long tables are automatically paginated by fpdf's auto-break.
func (g *ReportPDFGenerator) addTable(pdf *fpdf.Fpdf, headers []string, widths []float64, rows [][]string) {
	// Header row
	pdf.SetFont("Helvetica", "B", 9)
	pdf.SetFillColor(30, 58, 138)
	pdf.SetTextColor(255, 255, 255)
	for i, h := range headers {
		pdf.CellFormat(widths[i], 7, h, "1", 0, "C", true, 0, "")
	}
	pdf.Ln(-1)

	// Data rows
	pdf.SetFont("Helvetica", "", 8)
	pdf.SetTextColor(50, 50, 50)
	for idx, row := range rows {
		// Alternating row colours
		if idx%2 == 0 {
			pdf.SetFillColor(248, 250, 252)
		} else {
			pdf.SetFillColor(255, 255, 255)
		}
		for i, cell := range row {
			w := widths[0]
			if i < len(widths) {
				w = widths[i]
			}
			pdf.CellFormat(w, 6, cell, "1", 0, "L", true, 0, "")
		}
		pdf.Ln(-1)
	}
	pdf.Ln(4)
}

// addTrafficLight draws a filled circle at (x, y) coloured red, amber, or
// green to indicate risk/compliance status.
func (g *ReportPDFGenerator) addTrafficLight(pdf *fpdf.Fpdf, x, y float64, color string) {
	switch color {
	case "green":
		pdf.SetFillColor(34, 197, 94)
	case "amber":
		pdf.SetFillColor(234, 179, 8)
	case "red":
		pdf.SetFillColor(239, 68, 68)
	default:
		pdf.SetFillColor(200, 200, 200)
	}
	pdf.Circle(x, y, 3, "F")
}

// addWatermark renders a large, rotated, semi-transparent text across the
// current page (e.g. "DRAFT" or "CONFIDENTIAL").
func (g *ReportPDFGenerator) addWatermark(pdf *fpdf.Fpdf, text string) {
	pdf.SetFont("Helvetica", "B", 60)
	pdf.SetTextColor(230, 230, 230)
	pdf.TransformBegin()
	pdf.TransformRotate(45, 105, 148)
	pdf.Text(35, 190, text)
	pdf.TransformEnd()
}

// render writes the PDF document to a byte slice.
func (g *ReportPDFGenerator) render(pdf *fpdf.Fpdf) ([]byte, error) {
	var buf bytes.Buffer
	if err := pdf.Output(&buf); err != nil {
		return nil, fmt.Errorf("rendering PDF: %w", err)
	}
	return buf.Bytes(), nil
}

