// GenerateComplianceReport builds a full compliance status PDF.
//
// Expected data keys (map[string]interface{}):
//   - overall_score        float64
//   - frameworks_count     int/string
//   - total_controls       int/string
//   - overdue_remediations int/string
//   - framework_scores     []interface{} of maps
//   - gaps                 []interface{} of maps
func (g *ReportPDFGenerator) GenerateComplianceReport(data interface{}) ([]byte, error) {
	reportData, _ := data.(map[string]interface{})

	pdf := g.newDoc("Compliance Status Report")

	// Cover page
	g.addCoverPage(pdf, "Compliance Status Report", "Framework Compliance Assessment & Gap Analysis")

	// Table of contents
	g.addTableOfContents(pdf, []string{
		"Executive Summary",
		"Framework Compliance Scores",
		"Gap Analysis",
		"Appendix: Methodology",
	})

	// Executive Summary
	pdf.AddPage()
	g.addSectionTitle(pdf, "1. Executive Summary")

	overallScore := "\u2014"
	scoreColor := "amber"
	if s, ok := reportData["overall_score"].(float64); ok {
		overallScore = fmt.Sprintf("%.1f%%", s)
		if s >= 80 {
			scoreColor = "green"
		} else if s < 60 {
			scoreColor = "red"
		}
	}

	g.addKPIRow(pdf, []struct{ Label, Value, Color string }{
		{"Overall Compliance Score", overallScore, scoreColor},
		{"Frameworks Adopted", fmt.Sprintf("%v", reportData["frameworks_count"]), ""},
		{"Total Controls", fmt.Sprintf("%v", reportData["total_controls"]), ""},
		{"Overdue Remediations", fmt.Sprintf("%v", reportData["overdue_remediations"]), "red"},
	})

	g.addParagraph(pdf, "This report presents the current compliance status across all adopted "+
		"frameworks, including control implementation rates, maturity levels, and "+
		"identified gaps requiring remediation.")

	// Framework Scores
	g.addSectionTitle(pdf, "2. Framework Compliance Scores")

	if scores, ok := reportData["framework_scores"].([]interface{}); ok {
		headers := []string{"Framework", "Version", "Score", "Implemented", "Partial", "Gaps", "Maturity"}
		widths := []float64{45, 20, 20, 25, 25, 25, 30}
		var rows [][]string
		for _, s := range scores {
			if score, ok := s.(map[string]interface{}); ok {
				rows = append(rows, []string{
					fmt.Sprintf("%v", score["framework_name"]),
					fmt.Sprintf("%v", score["framework_version"]),
					fmt.Sprintf("%.1f%%", score["compliance_score"]),
					fmt.Sprintf("%v", score["implemented_count"]),
					fmt.Sprintf("%v", score["partial_count"]),
					fmt.Sprintf("%v", score["not_implemented_count"]),
					fmt.Sprintf("%.1f", score["avg_maturity_level"]),
				})
			}
		}
		g.addTable(pdf, headers, widths, rows)
	}

	// Gap Analysis
	g.addSectionTitle(pdf, "3. Gap Analysis")
	g.addParagraph(pdf, "The following controls are not yet fully implemented and require "+
		"attention. Controls are sorted by risk impact (critical first).")

	if gaps, ok := reportData["gaps"].([]interface{}); ok && len(gaps) > 0 {
		headers := []string{"Control", "Framework", "Status", "Risk", "Owner", "Due Date"}
		widths := []float64{25, 35, 30, 25, 40, 35}
		var rows [][]string
		limit := len(gaps)
		if limit > 30 {
			limit = 30
		}
		for _, gi := range gaps[:limit] {
			if gap, ok := gi.(map[string]interface{}); ok {
				rows = append(rows, []string{
					fmt.Sprintf("%v", gap["control_code"]),
					fmt.Sprintf("%v", gap["framework_name"]),
					fmt.Sprintf("%v", gap["status"]),
					fmt.Sprintf("%v", gap["risk_if_not_implemented"]),
					fmt.Sprintf("%v", gap["owner_name"]),
					fmt.Sprintf("%v", gap["remediation_due_date"]),
				})
			}
		}
		g.addTable(pdf, headers, widths, rows)
	}

	// Appendix
	pdf.AddPage()
	g.addSectionTitle(pdf, "Appendix: Methodology")
	g.addParagraph(pdf, "Compliance scores are calculated using a weighted formula: "+
		"Effective=1.0, Implemented=0.85, Partial=0.50, Planned=0.15, "+
		"Not Implemented=0.00. Controls marked as Not Applicable are excluded "+
		"from the denominator.")
	g.addParagraph(pdf, "Maturity levels follow the CMMI model: 0=Non-existent, "+
		"1=Initial, 2=Managed, 3=Defined, 4=Quantitatively Managed, 5=Optimizing.")

	return g.render(pdf)
}

// GenerateRiskReport builds a risk register PDF.
//
// Expected data keys (map[string]interface{}):
//   - total_risks               int/string
//   - critical_count            int/string
//   - avg_residual_score        float64
//   - treatment_completion_rate float64
//   - top_risks                 []interface{} of maps
//   - treatment_summary         map[string]interface{}
func (g *ReportPDFGenerator) GenerateRiskReport(data interface{}) ([]byte, error) {
	reportData, _ := data.(map[string]interface{})

	pdf := g.newDoc("Risk Register Report")
	g.addCoverPage(pdf, "Risk Register Report", "Enterprise Risk Assessment & Treatment Status")

	// Table of contents
	g.addTableOfContents(pdf, []string{
		"Risk Summary",
		"Top Risks by Residual Score",
		"Treatment Progress",
	})

	pdf.AddPage()
	g.addSectionTitle(pdf, "1. Risk Summary")

	totalRisks := fmt.Sprintf("%v", reportData["total_risks"])
	criticalCount := fmt.Sprintf("%v", reportData["critical_count"])
	avgScore := "\u2014"
	if s, ok := reportData["avg_residual_score"].(float64); ok {
		avgScore = fmt.Sprintf("%.1f", s)
	}
	treatmentRate := "\u2014"
	if s, ok := reportData["treatment_completion_rate"].(float64); ok {
		treatmentRate = fmt.Sprintf("%.0f%%", s)
	}

	g.addKPIRow(pdf, []struct{ Label, Value, Color string }{
		{"Total Active Risks", totalRisks, ""},
		{"Critical Risks", criticalCount, "red"},
		{"Avg Residual Score", avgScore, "amber"},
		{"Treatment Completion", treatmentRate, "green"},
	})

	// Top Risks
	g.addSectionTitle(pdf, "2. Top Risks by Residual Score")
	if risks, ok := reportData["top_risks"].([]interface{}); ok && len(risks) > 0 {
		headers := []string{"Ref", "Title", "Category", "Inherent", "Residual", "Level", "Owner"}
		widths := []float64{20, 50, 30, 20, 20, 20, 30}
		var rows [][]string
		for _, r := range risks {
			if risk, ok := r.(map[string]interface{}); ok {
				rows = append(rows, []string{
					fmt.Sprintf("%v", risk["risk_ref"]),
					fmt.Sprintf("%v", risk["title"]),
					fmt.Sprintf("%v", risk["category_name"]),
					fmt.Sprintf("%v", risk["inherent_risk_score"]),
					fmt.Sprintf("%v", risk["residual_risk_score"]),
					fmt.Sprintf("%v", risk["residual_risk_level"]),
					fmt.Sprintf("%v", risk["owner_name"]),
				})
			}
		}
		g.addTable(pdf, headers, widths, rows)
	}

	// Treatment Progress
	g.addSectionTitle(pdf, "3. Treatment Progress")
	g.addParagraph(pdf, "Summary of risk treatment plans and their current status.")

	if treatments, ok := reportData["treatment_summary"].(map[string]interface{}); ok {
		headers := []string{"Status", "Count"}
		widths := []float64{100, 90}
		rows := [][]string{
			{"Planned", fmt.Sprintf("%v", treatments["planned"])},
			{"In Progress", fmt.Sprintf("%v", treatments["in_progress"])},
			{"Completed", fmt.Sprintf("%v", treatments["completed"])},
			{"Overdue", fmt.Sprintf("%v", treatments["overdue"])},
		}
		g.addTable(pdf, headers, widths, rows)
	}

	return g.render(pdf)
}

// GenerateAuditReport builds an audit findings PDF.
//
// Expected data keys (map[string]interface{}):
//   - total_findings    int/string
//   - critical_findings int/string
//   - high_findings     int/string
//   - open_findings     int/string
//   - findings          []interface{} of maps
func (g *ReportPDFGenerator) GenerateAuditReport(data interface{}) ([]byte, error) {
	reportData, _ := data.(map[string]interface{})

	pdf := g.newDoc("Audit Findings Report")
	g.addCoverPage(pdf, "Audit Findings Report", "Internal Audit Assessment Results")

	// Table of contents
	g.addTableOfContents(pdf, []string{
		"Audit Summary",
		"Findings Detail",
	})

	pdf.AddPage()
	g.addSectionTitle(pdf, "1. Audit Summary")

	g.addKPIRow(pdf, []struct{ Label, Value, Color string }{
		{"Total Findings", fmt.Sprintf("%v", reportData["total_findings"]), ""},
		{"Critical", fmt.Sprintf("%v", reportData["critical_findings"]), "red"},
		{"High", fmt.Sprintf("%v", reportData["high_findings"]), "amber"},
		{"Open", fmt.Sprintf("%v", reportData["open_findings"]), "red"},
	})

	g.addSectionTitle(pdf, "2. Findings Detail")
	if findings, ok := reportData["findings"].([]interface{}); ok && len(findings) > 0 {
		headers := []string{"Ref", "Title", "Severity", "Status", "Due Date", "Responsible"}
		widths := []float64{22, 50, 22, 22, 30, 44}
		var rows [][]string
		for _, f := range findings {
			if finding, ok := f.(map[string]interface{}); ok {
				rows = append(rows, []string{
					fmt.Sprintf("%v", finding["finding_ref"]),
					fmt.Sprintf("%v", finding["title"]),
					fmt.Sprintf("%v", finding["severity"]),
					fmt.Sprintf("%v", finding["status"]),
					fmt.Sprintf("%v", finding["due_date"]),
					fmt.Sprintf("%v", finding["responsible_name"]),
				})
			}
		}
		g.addTable(pdf, headers, widths, rows)
	}

	return g.render(pdf)
}
